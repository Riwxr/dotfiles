	hl.exec_cmd("awww-daemon & awww img " .. x.wallpaper)
	hl.exec_cmd("mpd ~/.config/mpd/mpd.conf")
	hl.exec_cmd("ydotoold")
	hl.exec_cmd("elephant")
	hl.exec_cmd("hyprscratch init clean eager spotless")
	hl.exec_cmd("qs -p ~/.config/quickshell/clock/shell.qml -d")
	hl.exec_cmd("qs -p '/home/riwxr/dotfiles/.config/quickshell/idealshell/shell.qml' -d")
