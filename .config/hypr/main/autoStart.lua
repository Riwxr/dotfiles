	hl.exec_cmd("~/.config/hypr/autostart.sh")
	hl.exec_cmd("kdeconnectd & kdeconnect-indicator")
	hl.exec_cmd("swaync & hypridle & flameshot & hyprsunset & swayosd-server & squeekboard")
